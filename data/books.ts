export const featuredBooks = books.filter(
  (book) => book.featured
);

export const bestsellerBooks = books.filter(
  (book) => book.bestseller
);

export const upcomingBooks = books.filter(
  (book) => book.comingSoon
);

export function getBookBySlug(
  slug: string
): Book | undefined {
  return books.find((book) => book.slug === slug);
}

export function getBooksByCategory(
  category: string
): Book[] {
  return books.filter(
    (book) =>
      book.category.toLowerCase() ===
      category.toLowerCase()
  );
}

export function getRelatedBooks(
  slug: string,
  limit = 3
): Book[] {
  const currentBook = getBookBySlug(slug);

  if (!currentBook) return [];

  return books
    .filter(
      (book) =>
        book.slug !== slug &&
        book.category === currentBook.category
    )
    .slice(0, limit);
}